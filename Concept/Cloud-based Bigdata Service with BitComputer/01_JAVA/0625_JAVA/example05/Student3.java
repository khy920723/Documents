package com.khy.example05;

public class Student3 {

	private String name;
	private String sex;
	private int score;
	
	public Student3(String name, String sex, int score) {
		this.name = name;
		this.sex = sex;
		this.score = score;
	}
	
	/**
	 * @return the sex
	 */
	public String getSex() {
		return sex;
	}
	
	/**
	 * @return the score
	 */
	public int getScore() {
		return score;
	}
	
	
	
}
